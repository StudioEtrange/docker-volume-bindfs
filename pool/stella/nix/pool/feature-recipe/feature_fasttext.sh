	FEAT_LIST_SCHEMA="SNAPSHOT:source 0_1_0:source"
	FEAT_DEFAULT_ARCH=
	FEAT_DEFAULT_FLAVOUR=source
}


feature_fasttext_0_1_0() {
	FEAT_VERSION=0_1_0
	FEAT_SOURCE_DEPENDENCIES=
	FEAT_BINARY_DEPENDENCIES=

	FEAT_SOURCE_URL="https://github.com/facebookresearch/fastText/archive/v0.1.0.tar.gz"
	FEAT_SOURCE_URL_FILENAME="fasttext-v0.1.0.tar.gz"
	FEAT_SOURCE_URL_PROTOCOL=HTTP_ZIP

	FEAT_BINARY_URL=
	FEAT_BINARY_URL_FILENAME=
	FEAT_BINARY_URL_PROTOCOL=

	FEAT_SOURCE_CALLBACK=
	FEAT_BINARY_CALLBACK=
	FEAT_ENV_CALLBACK=

	FEAT_INSTALL_TEST="$FEAT_INSTALL_ROOT/fasttext"
	FEAT_SEARCH_PATH="$FEAT_INSTALL_ROOT"

}

